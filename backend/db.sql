CREATE TABLE Account
(
    account_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    account_email VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    role VARCHAR(20) NOT NULL CHECK (role IN ('Customer', 'Vendor', 'Operator', 'NEA'))
);

CREATE TABLE Customer
(
    customer_id UNIQUEIDENTIFIER PRIMARY KEY REFERENCES Account(account_id),
    customer_name VARCHAR(255) NOT NULL,
    loyalty_points SMALLINT NOT NULL,
);

CREATE TABLE Vendor
(
    vendor_id UNIQUEIDENTIFIER PRIMARY KEY REFERENCES Account(account_id)
);

CREATE TABLE Operator
(
    operator_id UNIQUEIDENTIFIER PRIMARY KEY REFERENCES Account(account_id)
);

CREATE TABLE NEA
(
    nea_id UNIQUEIDENTIFIER PRIMARY KEY REFERENCES Account(account_id)
);

-- NEW TABLE: HawkerCentre (must be created before Stall)
CREATE TABLE HawkerCentre
(
    hawker_centre_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    centre_name VARCHAR(255) NOT NULL,
    address VARCHAR(255) NOT NULL,
    operator_id UNIQUEIDENTIFIER NOT NULL REFERENCES Operator(operator_id)
);

-- UPDATED: added hawker_centre_id column
CREATE TABLE RefreshToken
(
    account_id UNIQUEIDENTIFIER PRIMARY KEY REFERENCES Account(account_id),
    refresh_token VARCHAR(MAX) NOT NULL,
    created_at DATETIME DEFAULT GETDATE(),
);

CREATE TABLE Stall
(
    stall_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    vendor_id UNIQUEIDENTIFIER NOT NULL REFERENCES Vendor(vendor_id),
    hawker_centre_id UNIQUEIDENTIFIER NULL REFERENCES HawkerCentre(hawker_centre_id),
    stall_name VARCHAR(255) NOT NULL,
    stall_unit_no CHAR(6) NOT NULL CHECK (stall_unit_no LIKE '#[0-9][0-9]-[0-9][0-9]')
);

CREATE TABLE Inspection
(
    inspection_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    nea_id UNIQUEIDENTIFIER NOT NULL REFERENCES NEA(nea_id),
    inspection_date DATETIME DEFAULT GETDATE(),
    score INT NOT NULL CHECK (score BETWEEN 0 AND 100),
    remarks TEXT,
    hygiene_grade CHAR(1) NOT NULL CHECK (hygiene_grade IN ('A', 'B', 'C', 'D'))
);

CREATE TABLE MenuItem
(
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    item_code UNIQUEIDENTIFIER DEFAULT NEWID() NOT NULL,
    item_desc TEXT, 
    item_price SMALLMONEY NOT NULL,
    item_category VARCHAR(255) NOT NULL CHECK (item_category IN ('Drinks', 'Dessert', 'Main', 'Sides')),
    CONSTRAINT PK_MenuItem PRIMARY KEY (stall_id, item_code)
);

CREATE TABLE Cuisine
(
    cuisine_id UNIQUEIDENTIFIER PRIMARY KEY,
    cuisine_desc VARCHAR(30) NOT NULL CHECK (cuisine_desc IN ('Korean', 'Western', 'Chinese', 'Japanese', 'Thai', 'Others'))
);

CREATE TABLE MenuItemCuisine
(
    stall_id UNIQUEIDENTIFIER NOT NULL,
    item_code UNIQUEIDENTIFIER NOT NULL,
    cuisine_id UNIQUEIDENTIFIER NOT NULL REFERENCES Cuisine(cuisine_id),
    CONSTRAINT PK_MenuItemCuisine PRIMARY KEY (cuisine_id, stall_id, item_code),
    CONSTRAINT FK_MenuItemCuisine_MenuItem FOREIGN KEY (stall_id, item_code) REFERENCES MenuItem(stall_id, item_code)
);

CREATE TABLE Orders
(
    order_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    customer_id UNIQUEIDENTIFIER NOT NULL REFERENCES Customer(customer_id),
    order_date DATETIME DEFAULT GETDATE(),
    total_amount SMALLMONEY NOT NULL,
    status VARCHAR(20) DEFAULT 'Pending' CHECK (status IN ('Pending', 'Preparing', 'Ready', 'Completed', 'Cancelled')),
    queue_number INT NOT NULL,
    is_eco_friendly_packaging BIT NOT NULL DEFAULT 0
);

CREATE TABLE OrderItem
(
    order_id UNIQUEIDENTIFIER NOT NULL REFERENCES Orders(order_id),
    stall_id UNIQUEIDENTIFIER NOT NULL,
    item_code UNIQUEIDENTIFIER NOT NULL,
    quantity INT NOT NULL CHECK (quantity > 0),
    PRIMARY KEY (order_id, stall_id, item_code),
    CONSTRAINT FK_OrderItem_MenuItem FOREIGN KEY (stall_id, item_code) REFERENCES MenuItem(stall_id, item_code)
);

CREATE TABLE Rating
(
    rating_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    customer_id UNIQUEIDENTIFIER NOT NULL REFERENCES Customer(customer_id),
    rating INT NOT NULL CHECK (rating BETWEEN 1 AND 5),
    comment TEXT,
    created_at DATETIME DEFAULT GETDATE()
);

CREATE TABLE Complaint
(
    complaint_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    customer_id UNIQUEIDENTIFIER NOT NULL REFERENCES Customer(customer_id),
    subject VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    status VARCHAR(20) DEFAULT 'Open' CHECK (status IN ('Open', 'Investigating', 'Resolved', 'Closed')),
    created_at DATETIME DEFAULT GETDATE()
);

CREATE TABLE Feedback
(
    feedback_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    customer_id UNIQUEIDENTIFIER NOT NULL REFERENCES Customer(customer_id),
    description TEXT NOT NULL,
    created_at DATETIME DEFAULT GETDATE()
);

CREATE TABLE Promotion
(
    promotion_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    title VARCHAR(255) NOT NULL,
    description TEXT,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL
);

-- NEW TABLE: RentalAgreement
CREATE TABLE RentalAgreement
(
    rental_agreement_id UNIQUEIDENTIFIER DEFAULT NEWID() PRIMARY KEY,
    stall_id UNIQUEIDENTIFIER NOT NULL REFERENCES Stall(stall_id),
    operator_id UNIQUEIDENTIFIER NOT NULL REFERENCES Operator(operator_id),
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    rental_fee SMALLMONEY NOT NULL,
    status VARCHAR(20) DEFAULT 'Active' CHECK (status IN ('Active', 'Expired', 'Terminated'))
);
GO

